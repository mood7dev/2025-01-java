package ch03.sec01;

public class SingleOperatorExample {
    public static void main(String[] args) {
        int i1 = 0;
        System.out.println(i1++);
        /* 반복문 */
        for (int i2 = 0; i2 < 8; i2++) {
            System.out.println(i2);
        }
    }
}

